/**
 * PDF Generator Service
 * 
 * Generates professional PDF reports for inventory and sales
 * Uses pdf-lib for PDF generation with farm branding
 */

import { PDFDocument, rgb, StandardFonts } from 'pdf-lib';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

/**
 * Generate inventory PDF report
 * @param {object} options - Report options
 * @param {string} options.farmId - Farm ID
 * @param {string} options.farmName - Farm name
 * @param {array} options.items - Inventory items
 * @param {string} options.category - Category filter
 * @param {boolean} options.includeValuation - Include pricing
 * @returns {Promise<Buffer>} PDF buffer
 */
export async function generateInventoryPDF(options) {
  const { farmId, farmName, items, category, includeValuation = true } = options;
  
  const pdfDoc = await PDFDocument.create();
  const font = await pdfDoc.embedFont(StandardFonts.Helvetica);
  const boldFont = await pdfDoc.embedFont(StandardFonts.HelveticaBold);
  
  let page = pdfDoc.addPage([612, 792]); // Letter size
  const { width, height } = page.getSize();
  const margin = 50;
  let y = height - margin;
  
  // Header
  page.drawText('INVENTORY REPORT', {
    x: margin,
    y: y,
    size: 24,
    font: boldFont,
    color: rgb(0.2, 0.5, 0.3)
  });
  
  y -= 30;
  page.drawText(farmName || `Farm ${farmId}`, {
    x: margin,
    y: y,
    size: 14,
    font: boldFont
  });
  
  y -= 20;
  const reportDate = new Date().toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric'
  });
  page.drawText(`Report Date: ${reportDate}`, {
    x: margin,
    y: y,
    size: 10,
    font: font
  });
  
  if (category) {
    y -= 15;
    page.drawText(`Category: ${category}`, {
      x: margin,
      y: y,
      size: 10,
      font: font
    });
  }
  
  y -= 40;
  
  // Table headers
  const headers = ['SKU', 'Product Name', 'Category', 'Available', 'Total'];
  if (includeValuation) {
    headers.push('Retail $', 'Value $');
  }
  
  const colWidths = includeValuation ? 
    [60, 150, 100, 60, 60, 60, 70] :
    [80, 180, 120, 80, 80];
  
  let x = margin;
  headers.forEach((header, i) => {
    page.drawText(header, {
      x: x,
      y: y,
      size: 9,
      font: boldFont
    });
    x += colWidths[i];
  });
  
  // Line under headers
  y -= 5;
  page.drawLine({
    start: { x: margin, y: y },
    end: { x: width - margin, y: y },
    thickness: 1,
    color: rgb(0.5, 0.5, 0.5)
  });
  
  y -= 15;
  
  // Data rows
  let totalValue = 0;
  let totalQty = 0;
  
  for (const item of items) {
    // Check if we need a new page
    if (y < 100) {
      page = pdfDoc.addPage([612, 792]);
      y = height - margin;
    }
    
    x = margin;
    const rowData = [
      item.sku_id || '',
      truncateText(item.product_name || '', 25),
      item.category || '',
      String(item.quantity_available || 0),
      String(item.quantity_total || 0)
    ];
    
    if (includeValuation) {
      const price = Number(item.price_per_unit || 0);
      const value = price * (item.quantity_total || 0);
      rowData.push(`$${price.toFixed(2)}`, `$${value.toFixed(2)}`);
      totalValue += value;
    }
    
    totalQty += Number(item.quantity_total || 0);
    
    rowData.forEach((text, i) => {
      page.drawText(text, {
        x: x,
        y: y,
        size: 8,
        font: font
      });
      x += colWidths[i];
    });
    
    y -= 15;
  }
  
  // Totals row
  y -= 10;
  page.drawLine({
    start: { x: margin, y: y + 10 },
    end: { x: width - margin, y: y + 10 },
    thickness: 1,
    color: rgb(0.5, 0.5, 0.5)
  });
  
  x = margin;
  page.drawText('TOTALS:', {
    x: x,
    y: y,
    size: 9,
    font: boldFont
  });
  
  x += colWidths[0] + colWidths[1] + colWidths[2];
  page.drawText(String(totalQty), {
    x: x + 10,
    y: y,
    size: 9,
    font: boldFont
  });
  
  if (includeValuation) {
    x += colWidths[3] + colWidths[4] + colWidths[5];
    page.drawText(`$${totalValue.toFixed(2)}`, {
      x: x,
      y: y,
      size: 9,
      font: boldFont
    });
  }
  
  // Footer
  const footer = `Generated by Light Engine Foxtrot • ${farmId}`;
  page.drawText(footer, {
    x: margin,
    y: 30,
    size: 8,
    font: font,
    color: rgb(0.5, 0.5, 0.5)
  });
  
  const pdfBytes = await pdfDoc.save();
  return Buffer.from(pdfBytes);
}

/**
 * Generate sales PDF report
 * @param {object} options - Report options
 * @param {string} options.farmId - Farm ID
 * @param {string} options.farmName - Farm name
 * @param {array} options.orders - Sales orders
 * @param {string} options.startDate - Start date
 * @param {string} options.endDate - End date
 * @param {string} options.channel - Sales channel
 * @returns {Promise<Buffer>} PDF buffer
 */
export async function generateSalesPDF(options) {
  const { farmId, farmName, orders, startDate, endDate, channel } = options;
  
  const pdfDoc = await PDFDocument.create();
  const font = await pdfDoc.embedFont(StandardFonts.Helvetica);
  const boldFont = await pdfDoc.embedFont(StandardFonts.HelveticaBold);
  
  let page = pdfDoc.addPage([612, 792]);
  const { width, height } = page.getSize();
  const margin = 50;
  let y = height - margin;
  
  // Header
  page.drawText('SALES REPORT', {
    x: margin,
    y: y,
    size: 24,
    font: boldFont,
    color: rgb(0.2, 0.5, 0.3)
  });
  
  y -= 30;
  page.drawText(farmName || `Farm ${farmId}`, {
    x: margin,
    y: y,
    size: 14,
    font: boldFont
  });
  
  y -= 20;
  page.drawText(`Period: ${startDate} to ${endDate}`, {
    x: margin,
    y: y,
    size: 10,
    font: font
  });
  
  if (channel && channel !== 'all') {
    y -= 15;
    page.drawText(`Channel: ${channel.toUpperCase()}`, {
      x: margin,
      y: y,
      size: 10,
      font: font
    });
  }
  
  y -= 40;
  
  // Summary boxes
  const totalRevenue = orders.reduce((sum, o) => sum + Number(o.total_amount || 0), 0);
  const totalTax = orders.reduce((sum, o) => sum + Number(o.tax_amount || 0), 0);
  const totalTips = orders.reduce((sum, o) => sum + Number(o.tip_amount || 0), 0);
  
  const boxWidth = 150;
  const boxHeight = 50;
  const gap = 20;
  
  // Revenue box
  page.drawRectangle({
    x: margin,
    y: y - boxHeight,
    width: boxWidth,
    height: boxHeight,
    borderColor: rgb(0.2, 0.5, 0.3),
    borderWidth: 2
  });
  
  page.drawText('Total Revenue', {
    x: margin + 10,
    y: y - 20,
    size: 10,
    font: font
  });
  
  page.drawText(`$${totalRevenue.toFixed(2)}`, {
    x: margin + 10,
    y: y - 40,
    size: 16,
    font: boldFont,
    color: rgb(0.2, 0.5, 0.3)
  });
  
  // Orders box
  page.drawRectangle({
    x: margin + boxWidth + gap,
    y: y - boxHeight,
    width: boxWidth,
    height: boxHeight,
    borderColor: rgb(0.2, 0.5, 0.3),
    borderWidth: 2
  });
  
  page.drawText('Total Orders', {
    x: margin + boxWidth + gap + 10,
    y: y - 20,
    size: 10,
    font: font
  });
  
  page.drawText(String(orders.length), {
    x: margin + boxWidth + gap + 10,
    y: y - 40,
    size: 16,
    font: boldFont,
    color: rgb(0.2, 0.5, 0.3)
  });
  
  // Tax box
  page.drawRectangle({
    x: margin + (boxWidth + gap) * 2,
    y: y - boxHeight,
    width: boxWidth,
    height: boxHeight,
    borderColor: rgb(0.2, 0.5, 0.3),
    borderWidth: 2
  });
  
  page.drawText('Sales Tax', {
    x: margin + (boxWidth + gap) * 2 + 10,
    y: y - 20,
    size: 10,
    font: font
  });
  
  page.drawText(`$${totalTax.toFixed(2)}`, {
    x: margin + (boxWidth + gap) * 2 + 10,
    y: y - 40,
    size: 16,
    font: boldFont,
    color: rgb(0.2, 0.5, 0.3)
  });
  
  y -= boxHeight + 40;
  
  // Table headers
  const headers = ['Date', 'Order ID', 'Customer', 'Subtotal', 'Tax', 'Total'];
  const colWidths = [70, 100, 150, 70, 60, 70];
  
  let x = margin;
  headers.forEach((header, i) => {
    page.drawText(header, {
      x: x,
      y: y,
      size: 9,
      font: boldFont
    });
    x += colWidths[i];
  });
  
  y -= 5;
  page.drawLine({
    start: { x: margin, y: y },
    end: { x: width - margin, y: y },
    thickness: 1,
    color: rgb(0.5, 0.5, 0.5)
  });
  
  y -= 15;
  
  // Data rows
  for (const order of orders) {
    if (y < 100) {
      page = pdfDoc.addPage([612, 792]);
      y = height - margin;
    }
    
    const orderDate = new Date(order.created_at || order.timestamp);
    const dateStr = orderDate.toLocaleDateString('en-US', { month: 'short', day: 'numeric' });
    
    x = margin;
    const rowData = [
      dateStr,
      order.order_id || '',
      truncateText(order.customer_name || 'Guest', 20),
      `$${Number(order.subtotal || 0).toFixed(2)}`,
      `$${Number(order.tax_amount || 0).toFixed(2)}`,
      `$${Number(order.total_amount || 0).toFixed(2)}`
    ];
    
    rowData.forEach((text, i) => {
      page.drawText(text, {
        x: x,
        y: y,
        size: 8,
        font: font
      });
      x += colWidths[i];
    });
    
    y -= 15;
  }
  
  // Footer
  const footer = `Generated by Light Engine Foxtrot • ${farmId}`;
  page.drawText(footer, {
    x: margin,
    y: 30,
    size: 8,
    font: font,
    color: rgb(0.5, 0.5, 0.5)
  });
  
  const pdfBytes = await pdfDoc.save();
  return Buffer.from(pdfBytes);
}

/**
 * Truncate text to fit column width
 */
function truncateText(text, maxLength) {
  if (text.length <= maxLength) return text;
  return text.substring(0, maxLength - 3) + '...';
}

export default {
  generateInventoryPDF,
  generateSalesPDF
};
